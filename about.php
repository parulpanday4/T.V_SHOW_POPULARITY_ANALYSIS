<p>Reality TV is the new mantra of television producers and channel executives. It is the means to increase TRP ratings and the end is always to outdo the other channels and the “similar-but-tweaked-here-and-there” shows churned out by the competition. Most of the television shows, which are being telecast nowadays, are reality shows specializing in dancing, singing, and acting. </p>
<p>Everything now is a competition. So fierce is the competition in this segment that every channel boasts of at least two to three reality shows. Some of them are inherited legally from abroad, (mostly and always from the USA the Godmother of reality television) or some are cheap copies of the shows abroad. The Indian reality shows have also been consistently successful in offering a wide variety. From Talent Hunt Shows, to dance dramas, to acting flicks, talk shows, chat shows, cookery shows.... the „reality hunt list‟ is endless.</p>


